package model;

public class Musica {
    
    private String nome;
    private Integer segundos;

    public Musica(){
        
    }

    public String getNome() {
        return nome;
    }
    public void setNome(String nome) {
        this.nome = nome;
    }
    public Integer getSegundos() {
        return segundos;
    }
    public void setSegundos(Integer segundos) {
        this.segundos = segundos;
    }

    

}
